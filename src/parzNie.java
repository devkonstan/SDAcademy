public class parzNie {
    public static void main(String[] args) {
        for (int i = 0; i <= 100; i++) {
            if (i % 2 == 0) {
                System.out.println("liczba " + i + " jest parzysta");
            } else {
                System.out.println("liczba " + i + " jest nieparzysta");
            }
        }
    }
}
